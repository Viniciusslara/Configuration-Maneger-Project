    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTableClientes.getModel();
        modelo.setNumRows(0);
        ClienteDAO clienteDao = new ClienteDAO();
        
        for(Cliente client: clienteDao.read()){
            modelo.addRow(new Object[]{
                client.getId(),
                client.getNome(),
                client.getDataNasc(),
                client.getTelefone(),
                client.getCpf(),
                client.getCnh()
            });
            
        }
    }
    
    

