    private void jTableVeiculosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableVeiculosMouseClicked
        // TODO add your handling code here:
        
        if(jTableVeiculos.getSelectedRow() != -1){
            
            jTextFieldMarca.setText(jTableVeiculos.getValueAt(jTableVeiculos.getSelectedRow(), 1).toString());
            jTextFieldModelo.setText(jTableVeiculos.getValueAt(jTableVeiculos.getSelectedRow(), 2).toString());
            jTextFieldPlaca.setText(jTableVeiculos.getValueAt(jTableVeiculos.getSelectedRow(), 3).toString());
            jTextFieldVD.setText(jTableVeiculos.getValueAt(jTableVeiculos.getSelectedRow(), 4).toString());
           
        }
        
    }//GEN-LAST:event_jTableVeiculosMouseClicked

    private void jButtonATTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonATTActionPerformed
        // TODO add your handling code here:
        
        if(jTableVeiculos.getSelectedRow() != -1){
            
            Veiculos veiculo = new Veiculos();
            VeiculosDAO veiculoDao = new VeiculosDAO();
        
            veiculo.setId(Integer.parseInt(jTableVeiculos.getValueAt(jTableVeiculos.getSelectedRow(), 0).toString()));
            veiculo.setMarca(jTextFieldMarca.getText());
            veiculo.setModelo(jTextFieldModelo.getText());
            veiculo.setPlaca(jTextFieldPlaca.getText());
            veiculo.setDiaria(Float.parseFloat(jTextFieldVD.getText()));
        
            veiculoDao.update(veiculo);
            
            jTextFieldMarca.setText("");
            jTextFieldModelo.setText("");
            jTextFieldPlaca.setText("");
            jTextFieldVD.setText("");
            
            readJTable();
            
        }
    }//GEN-LAST:event_jButtonATTActionPerformed

    private void jButtonCad2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCad2ActionPerformed
        // TODO add your handling code here:
        
        if(jTableVeiculos.getSelectedRow() != -1){
            
            Veiculos veiculo = new Veiculos();
            VeiculosDAO veiculoDao = new VeiculosDAO();
        
            veiculo.setId(Integer.parseInt(jTableVeiculos.getValueAt(jTableVeiculos.getSelectedRow(), 0).toString()));
        
            veiculoDao.delete(veiculo);
            
            jTextFieldMarca.setText("");
            jTextFieldModelo.setText("");
            jTextFieldPlaca.setText("");
            jTextFieldVD.setText("");
            
            readJTable();
            
        }
    }//GEN-LAST:event_jButtonCad2ActionPerformed

    public void readJTable(){
        
        DefaultTableModel modelo = (DefaultTableModel) jTableVeiculos.getModel();
        modelo.setNumRows(0);
        VeiculosDAO veiculoDao = new VeiculosDAO();
        
        for(Veiculos veiculos: veiculoDao.read()){
            modelo.addRow(new Object[]{
                veiculos.getId(),
                veiculos.getMarca(),
                veiculos.getModelo(),
                veiculos.getPlaca(),
                veiculos.getDiaria(),
                veiculos.getDisponibilidade(),
            });
            
        }
    }
    

